public class Rezolutie {
    private int latime;
    private int inaltime;

    public Rezolutie(int latime, int inaltime) {
        this.latime = latime;
        this.inaltime = inaltime;
    }

    public int getLatime() {
        return latime;
    }

    public int getInaltime() {
        return inaltime;
    }
}
