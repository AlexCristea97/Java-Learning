public class Dimensiuni {
    private int latime;
    private int inaltime;
    private int adancime;


    public Dimensiuni(int latime, int inaltime, int adancime) {
        this.latime = latime;
        this.inaltime = inaltime;
        this.adancime = adancime;
    }

    public int getLatime() {
        return latime;
    }

    public int getInaltime() {
        return inaltime;
    }

    public int getAdancime() {
        return adancime;
    }
}
